# -*- coding: utf-8 -*-
"""
Модуль с классами для моделирования планет.
Содержит классы: Планета и СолнечнаяСистема.
"""

import math
import random
from data import ДАННЫЕ_ПЛАНЕТ, БАЗОВАЯ_СКОРОСТЬ


class Планета:
    """
    Класс, представляющий планету в Солнечной системе.
    
    Атрибуты:
        имя: название планеты
        радиус_орбиты: расстояние от Солнца (в пикселях)
        физический_радиус: размер планеты (в пикселях)
        цвет: цвет планеты
        угол: текущий угол на орбите
        период: период обращения (в годах Земли)
        скорость: угловая скорость (градусов в секунду)
        температура: температура на поверхности
        атмосфера: состав атмосферы
        описание: краткое описание планеты
    """
    
    def __init__(self, имя, радиус_орбиты, физический_радиус, цвет, период):
        """
        Конструктор класса Планета.
        
        Аргументы:
            имя: название планеты
            радиус_орбиты: расстояние от Солнца в пикселях
            физический_радиус: размер планеты в пикселях
            цвет: цвет планеты
            период: период обращения в годах
        """
        self.имя = имя
        self.радиус_орбиты = радиус_орбиты
        self.физический_радиус = физический_радиус
        self.цвет = цвет
        self.период = период
        self.угол = random.uniform(0, 360)  # Начальный угол
        self.базовая_скорость = БАЗОВАЯ_СКОРОСТЬ
        self.скорость = self.базовая_скорость / период
        
        # Дополнительные данные из словаря
        данные = ДАННЫЕ_ПЛАНЕТ.get(имя, {})
        self.температура = данные.get("температура", "неизвестно")
        self.атмосфера = данные.get("атмосфера", "неизвестна")
        self.описание = данные.get("описание", "Нет описания")
    
    def обновить_позицию(self, множитель_скорости=1.0):
        """
        Обновляет положение планеты на орбите.
        
        Аргументы:
            множитель_скорости: множитель скорости движения (по умолчанию 1.0)
        """
        self.угол += self.скорость * множитель_скорости * 0.5
        if self.угол > 360:
            self.угол -= 360
    
    def получить_координаты(self, центр_x, центр_y):
        """
        Возвращает координаты планеты на экране.
        
        Аргументы:
            центр_x: координата X центра (Солнца)
            центр_y: координата Y центра (Солнца)
            
        Возвращает:
            Кортеж (x, y) координат планеты
        """
        радиан = math.radians(self.угол)
        x = центр_x + self.радиус_орбиты * math.cos(радиан)
        y = центр_y + self.радиус_орбиты * math.sin(радиан)
        return x, y
    
    def установить_скорость(self, новая_базовая_скорость):
        """
        Устанавливает новую базовую скорость.
        
        Аргументы:
            новая_базовая_скорость: новая базовая скорость (для Земли)
        """
        self.базовая_скорость = новая_базовая_скорость
        self.скорость = self.базовая_скорость / self.период


class СолнечнаяСистема:
    """
    Класс, управляющий всеми планетами в Солнечной системе.
    
    Атрибуты:
        планеты: список объектов Планета
        скорость: множитель скорости движения
    """
    
    def __init__(self, центр_x, центр_y, масштаб=1.0):
        """
        Конструктор класса СолнечнаяСистема.
        
        Аргументы:
            центр_x: координата X центра (Солнца)
            центр_y: координата Y центра (Солнца)
            масштаб: масштаб отображения
        """
        self.центр_x = центр_x
        self.центр_y = центр_y
        self.планеты = []
        self.скорость = 1.0
        self.масштаб = масштаб
        
        self._создать_планеты()
    
    def _создать_планеты(self):
        """Создаёт планеты на основе данных."""
        # Параметры орбит в пикселях
        орбиты = {
            "Меркурий": 50,
            "Венера": 75,
            "Земля": 100,
            "Марс": 130,
            "Юпитер": 190,
            "Сатурн": 250,
            "Уран": 310,
            "Нептун": 370
        }
        
        # Размеры планет в пикселях
        размеры = {
            "Меркурий": 5,
            "Венера": 8,
            "Земля": 9,
            "Марс": 7,
            "Юпитер": 20,
            "Сатурн": 17,
            "Уран": 12,
            "Нептун": 11
        }
        
        for имя, данные in ДАННЫЕ_ПЛАНЕТ.items():
            планета = Планета(
                имя=имя,
                радиус_орбиты=орбиты.get(имя, 100) * self.масштаб,
                физический_радиус=размеры.get(имя, 8) * self.масштаб,
                цвет=данные.get("цвет", "#888888"),
                период=данные.get("период", 1.0)
            )
            self.планеты.append(планета)
    
    def обновить(self):
        """Обновляет положение всех планет."""
        for планета in self.планеты:
            планета.обновить_позицию(self.скорость)
    
    def установить_скорость(self, скорость):
        """
        Устанавливает множитель скорости движения.
        
        Аргументы:
            скорость: новый множитель скорости (0.1 - 5.0)
        """
        self.скорость = скорость
    
    def найти_планету_по_координатам(self, x, y):
        """
        Находит планету по координатам клика.
        
        Аргументы:
            x: координата X клика
            y: координата Y клика
            
        Возвращает:
            Планету, если клик попал на неё, иначе None
        """
        for планета in self.планеты:
            px, py = планета.получить_координаты(self.центр_x, self.центр_y)
            расстояние = ((x - px) ** 2 + (y - py) ** 2) ** 0.5
            if расстояние <= планета.физический_радиус + 5:
                return планета
        return None