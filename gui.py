# -*- coding: utf-8 -*-
"""
Модуль графического интерфейса для программы о Солнечной системе.
Использует библиотеку Tkinter.
"""

import tkinter as tk
from tkinter import ttk, messagebox
import math
from data import ДАННЫЕ_ПЛАНЕТ


class СолнечнаяСистемаGUI:
    """
    Класс графического интерфейса программы.
    
    Атрибуты:
        root: корневое окно Tkinter
        canvas: холст для рисования
        солнечная_система: объект СолнечнаяСистема
        ширина: ширина окна
        высота: высота окна
        информация_метка: метка для отображения информации
        ползунок_скорости: ползунок регулировки скорости
    """
    
    def __init__(self):
        """Конструктор класса. Создаёт окно и инициализирует интерфейс."""
        self.root = tk.Tk()
        self.root.title("Солнечная система")
        self.root.geometry("1000x750")
        self.root.resizable(False, False)
        
        self.ширина = 900
        self.высота = 650
        self.центр_x = 350
        self.центр_y = 325
        
        # Создание элементов интерфейса
        self._создать_холст()
        self._создать_информационную_панель()
        self._создать_управление()
        
        # Инициализация Солнечной системы
        from planets import СолнечнаяСистема
        self.солнечная_система = СолнечнаяСистема(
            self.центр_x, 
            self.центр_y,
            масштаб=1.0
        )
        
        # Привязка событий
        self.canvas.bind("<Button-1>", self.обработать_клик)
        
        # Запуск анимации
        self.анимировать()
    
    def _создать_холст(self):
        """Создаёт холст для рисования Солнечной системы."""
        self.canvas = tk.Canvas(
            self.root,
            width=self.ширина,
            height=self.высота,
            bg="#000020"
        )
        self.canvas.grid(row=0, column=0, rowspan=3, padx=10, pady=10)
    
    def _создать_информационную_панель(self):
        """Создаёт панель для отображения информации о планете."""
        frame = tk.LabelFrame(self.root, text="Информация о планете", width=300, height=200)
        frame.grid(row=0, column=1, padx=10, pady=10, sticky="n")
        
        self.имя_планеты = tk.Label(frame, text="Нажмите на планету", font=("Arial", 14, "bold"))
        self.имя_планеты.pack(pady=5)
        
        self.информация_текст = tk.Text(frame, width=35, height=10, wrap=tk.WORD)
        self.информация_текст.pack(pady=5, padx=5)
        self.информация_текст.config(state=tk.DISABLED)
    
    def _создать_управление(self):
        """Создаёт элементы управления скоростью."""
        frame = tk.LabelFrame(self.root, text="Управление", width=300, height=150)
        frame.grid(row=1, column=1, padx=10, pady=10, sticky="n")
        
        # Ползунок скорости
        tk.Label(frame, text="Скорость движения:").pack(pady=5)
        
        self.ползунок_скорости = tk.Scale(
            frame,
            from_=0.1,
            to=5.0,
            resolution=0.1,
            orient=tk.HORIZONTAL,
            length=200,
            command=self.изменить_скорость
        )
        self.ползунок_скорости.set(1.0)
        self.ползунок_скорости.pack(pady=5)
        
        # Метка текущей скорости
        self.метка_скорости = tk.Label(frame, text="Скорость: 1.0x")
        self.метка_скорости.pack(pady=5)
        
        # Кнопка сброса
        btn_сброс = tk.Button(frame, text="Сбросить скорость", command=self.сбросить_скорость)
        btn_сброс.pack(pady=5)
        
        # Легенда
        legend_frame = tk.LabelFrame(self.root, text="Легенда", width=300, height=150)
        legend_frame.grid(row=2, column=1, padx=10, pady=10, sticky="n")
        
        # Создание легенды с цветами планет
        for i, (имя, данные) in enumerate(ДАННЫЕ_ПЛАНЕТ.items()):
            color = данные.get("цвет", "#888888")
            label = tk.Label(legend_frame, text=имя, fg=color, font=("Arial", 10))
            label.grid(row=i // 4, column=i % 4, padx=5, pady=2, sticky="w")
    
    def _создать_кнопку_информации(self):
        """Создаёт кнопку для отображения справки."""
        btn_справка = tk.Button(
            self.root,
            text="Справка",
            command=self.показать_справку,
            width=15
        )
        btn_справка.grid(row=2, column=1, padx=10, pady=5)
    
    def отрисовать(self):
        """Отрисовывает все элементы Солнечной системы."""
        self.canvas.delete("all")
        
        # Отрисовка орбит
        for планета in self.солнечная_система.планеты:
            радиус = планета.радиус_орбиты
            self.canvas.create_oval(
                self.центр_x - радиус,
                self.центр_y - радиус,
                self.центр_x + радиус,
                self.центр_y + радиус,
                outline="#333366",
                width=1
            )
        
        # Отрисовка Солнца
        self.canvas.create_oval(
            self.центр_x - 30,
            self.центр_y - 30,
            self.центр_x + 30,
            self.центр_y + 30,
            fill="#FFD700",
            outline="#FFA500",
            width=2
        )
        self.canvas.create_text(
            self.центр_x,
            self.центр_y + 40,
            text="Солнце",
            fill="#FFD700",
            font=("Arial", 10)
        )
        
        # Отрисовка планет
        for планета in self.солнечная_система.планеты:
            x, y = планета.получить_координаты(self.центр_x, self.центр_y)
            
            # Свечение планеты
            self.canvas.create_oval(
                x - планета.физический_радиус - 3,
                y - планета.физический_радиус - 3,
                x + планета.физический_радиус + 3,
                y + планета.физический_радиус + 3,
                fill=планета.цвет,
                outline=планета.цвет,
                width=1,
                stipple="gray50",
                tags=("planet", планета.имя)
            )
            
            # Сама планета
            self.canvas.create_oval(
                x - планета.физический_радиус,
                y - планета.физический_радиус,
                x + планета.физический_радиус,
                y + планета.физический_радиус,
                fill=планета.цвет,
                outline="white",
                width=1,
                tags=("planet", планета.имя)
            )
            
            # Имя планеты
            self.canvas.create_text(
                x,
                y + планета.физический_радиус + 12,
                text=планета.имя,
                fill=планета.цвет,
                font=("Arial", 8),
                tags=("planet", планета.имя)
            )
    
    def анимировать(self):
        """Выполняет анимацию движения планет."""
        self.солнечная_система.обновить()
        self.отрисовать()
        self.root.after(50, self.анимировать)  # 20 FPS
    
    def изменить_скорость(self, значение):
        """
        Изменяет скорость движения планет.
        
        Аргументы:
            значение: новое значение скорости (строка)
        """
        скорость = float(значение)
        self.солнечная_система.установить_скорость(скорость)
        self.метка_скорости.config(text=f"Скорость: {скорость:.1f}x")
    
    def сбросить_скорость(self):
        """Сбрасывает скорость к значению 1.0."""
        self.ползунок_скорости.set(1.0)
        self.изменить_скорость("1.0")
    
    def обработать_клик(self, событие):
        """
        Обрабатывает клик на холсте.
        
        Аргументы:
            событие: событие клика
        """
        x = событие.x
        y = событие.y
        
        # Проверка попадания в Солнце
        расстояние_до_солнца = ((x - self.центр_x) ** 2 + (y - self.центр_y) ** 2) ** 0.5
        if расстояние_до_солнца <= 30:
            self.показать_информацию_о_солнце()
            return
        
        # Поиск планеты по координатам
        планета = self.солнечная_система.найти_планету_по_координатам(x, y)
        if планета:
            self.показать_информацию(планета)
    
    def показать_информацию(self, планета):
        """
        Отображает информацию о планете.
        
        Аргументы:
            планета: объект Планета
        """
        self.имя_планеты.config(text=планета.имя, fg=планета.цвет)
        
        информация = f"""
Орбитальные характеристики:
• Расстояние от Солнца: {self.получить_расстояние(планета)}
• Период обращения: {планета.период} земных лет

Физические характеристики:
• Радиус: {self.получить_радиус(планета)}

Температура: {планета.температура}

Атмосфера:
{планета.атмосфера}

Описание:
{планета.описание}
"""
        self.информация_текст.config(state=tk.NORMAL)
        self.информация_текст.delete("1.0", tk.END)
        self.информация_текст.insert("1.0", информация)
        self.информация_текст.config(state=tk.DISABLED)
    
    def показать_информацию_о_солнце(self):
        """Отображает информацию о Солнце."""
        self.имя_планеты.config(text="Солнце", fg="#FFD700")
        
        информация = """
Звезда - жёлтый карлик.

Характеристики:
• Температура поверхности: ~5500°C
• Диаметр: 1 392 700 км
• Масса: 1.989 × 10^30 кг
• Возраст: ~4.6 млрд лет

Состав:
• Водород: 73%
• Гелий: 25%
• Другие элементы: 2%
"""
        self.информация_текст.config(state=tk.NORMAL)
        self.информация_текст.delete("1.0", tk.END)
        self.информация_текст.insert("1.0", информация)
        self.информация_текст.config(state=tk.DISABLED)
    
    def получить_расстояние(self, планета):
        """
        Возвращает расстояние от Солнца в понятном формате.
        
        Аргументы:
            планета: объект Планета
            
        Возвращает:
            Строка с расстоянием
        """
        данные = ДАННЫЕ_ПЛАНЕТ.get(планета.имя, {})
        расстояние = данные.get("расстояние", "неизвестно")
        return f"{расстояние} а.е."
    
    def получить_радиус(self, планета):
        """
        Возвращает радиус планеты в понятном формате.
        
        Аргументы:
            планета: объект Планета
            
        Возвращает:
            Строка с радиусом
        """
        данные = ДАННЫЕ_ПЛАНЕТ.get(планета.имя, {})
        радиус = данные.get("радиус", "неизвестно")
        return f"{радиус} радиусов Земли"
    
    def показать_справку(self):
        """Показывает справку с инструкцией."""
        справка = """
Информационная программа "Солнечная система"

Управление:
• Нажмите на планету - отобразится информация о ней
• Нажмите на Солнце - отобразится информация о Солнце
• Ползунок скорости - регулирует скорость движения всех планет
• Кнопка "Сбросить скорость" - возвращает скорость к 1.0

Особенности:
• Планеты движутся по орбитам с разной скоростью
• Скорость каждой планеты пропорциональна её периоду обращения
• Все планеты движутся согласованно относительно скорости Земли
        """
        messagebox.showinfo("Справка", справка)
    
    def запустить(self):
        """Запускает главный цикл программы."""
        self.root.mainloop()