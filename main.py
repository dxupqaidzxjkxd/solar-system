import sys
from gui import СолнечнаяСистемаGUI


def main():
    """
    Главная функция программы.
    Создаёт экземпляр GUI и запускает приложение.
    """
    try:
        app = СолнечнаяСистемаGUI()
        app.запустить()
    except KeyboardInterrupt:
        print("\nПрограмма завершена пользователем.")
        sys.exit(0)
    except Exception as e:
        print(f"Произошла ошибка: {e}")
        sys.exit(1)


if __name__ == "__main__":
    main()